<p>\subsection{Sistema de primer orden:} Dado un sistema de $n$ ecuaciones de primer orden tal como \begin{align*} \frac{dx_1}{dt}&amp;= g_1(t, x_1, x_2, \cdots, x_n)\ \frac{dx_2}{dt}&amp;= g_2(t, x_1, x_2, \cdots, x_n)\ &amp;\hspace{4em} \vdots \ \frac{dx_n}{dt}&amp;= g_n(t, x_1, x_2, \cdots, x_n) \end{align*} se denomina sistema de primer orden.</p>
<p>EJEMPLO</p>
<p>Este es un sistema de $3$ ecuaciones de \textbf{primer orden}</p>
